public class Typecasting {
    public static void main(String[]args){
        int m = 45;
        double mydouble = m;
        double n = 98.45;
        int y = (int)n;
        System.out.println(m);
        System.out.println(mydouble); //widening casting
        System.out.println(n);
        System.out.println(y);// narrowing casting
    
    }
    
}
